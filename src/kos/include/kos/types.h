#ifndef KOS_TYPES_H
#define KOS_TYPES_H

#include <inttypes.h>
#include <stdbool.h>
#include <stddef.h>

typedef int8_t i8;
typedef int16_t i16;
typedef int32_t i32;
typedef int64_t i64;

typedef uint8_t u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef uint64_t u64;

typedef float f32;
typedef double f64;

typedef ptrdiff_t isize;
typedef size_t usize;

typedef intptr_t iptr;
typedef uintptr_t uptr;

#endif // KOS_TYPES_H
